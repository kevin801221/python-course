"""
系統資訊檢測器 - System Info Detector
=====================================

【學習目標】
這個範例讓你驗證 Python 環境是否安裝成功，
並展示 Python 如何獲取系統資訊。

【前置知識】
- 第 01 課：安裝 Python 環境

【程式碼難度】★☆☆☆☆ 入門級

這個範例可以直接執行，不需要輸入任何內容。
如果你能看到輸出結果，代表你的 Python 環境安裝成功了！
"""

# ============================================================
# 第一步：匯入必要的模組
# ============================================================
# Python 提供了很多內建模組，可以幫助我們完成各種任務
# import 語句用來載入這些模組
# 現在不需要完全理解，後面的課程會詳細說明

import sys          # sys 模組：提供 Python 直譯器的資訊
import platform     # platform 模組：提供作業系統的資訊

# ============================================================
# 第二步：顯示歡迎訊息
# ============================================================
# print() 是 Python 最基本的輸出函式
# 可以把括號內的內容顯示在螢幕上

print("=" * 50)  # 印出 50 個等號作為分隔線
print("       Python 環境檢測工具")
print("=" * 50)
print()  # 印出空行

# ============================================================
# 第三步：顯示 Python 版本資訊
# ============================================================
# platform.python_version() 會返回 Python 的版本號
# 例如：3.10.10 或 3.11.5

print("【Python 環境資訊】")
print("-" * 50)

# 取得 Python 版本
python_version = platform.python_version()
print(f"Python 版本: {python_version}")

# 取得 Python 執行檔的路徑
python_path = sys.executable
print(f"Python 路徑: {python_path}")

# ============================================================
# 第四步：顯示作業系統資訊
# ============================================================
print()
print("【作業系統資訊】")
print("-" * 50)

# 取得作業系統名稱（Windows、Darwin、Linux）
os_name = platform.system()
print(f"作業系統: {os_name}")

# 取得系統版本
os_version = platform.version()
print(f"系統版本: {os_version}")

# 取得電腦架構（x86_64、arm64 等）
machine = platform.machine()
print(f"電腦架構: {machine}")

# ============================================================
# 第五步：顯示成功訊息
# ============================================================
print()
print("=" * 50)
print("  恭喜！你的 Python 環境安裝成功！")
print("  現在可以開始學習 Python 程式設計了")
print("=" * 50)


# ============================================================
# 【知識補充】
# ============================================================
"""
這個範例用到的概念：

1. import 語句
   - 用來載入 Python 的模組
   - 模組是已經寫好的程式碼，可以直接使用
   - 例如：import sys 載入 sys 模組

2. print() 函式
   - 把內容輸出到螢幕
   - 這是你學會的第一個 Python 函式

3. 變數
   - 例如：python_version = platform.python_version()
   - 等號左邊是變數名稱，右邊是要儲存的值
   - 變數會在第 03 課詳細說明

4. f-string（格式化字串）
   - f"文字 {變數} 文字" 可以把變數的值嵌入字串中
   - 這會在後面的課程詳細說明
"""


