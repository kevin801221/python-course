"""
個人資料管理器 - Personal Data Manager
======================================

【學習目標】
這個範例展示如何使用不同類型的變數來儲存個人資料。
透過這個範例，你將學會：
1. 字串（str）：儲存文字
2. 整數（int）：儲存整數
3. 浮點數（float）：儲存小數
4. 布林值（bool）：儲存 True/False
5. 變數的命名和使用

【前置知識】
- 第 02 課：print()、input()
- 第 03 課：變數

【程式碼難度】★☆☆☆☆ 入門級
"""

# ============================================================
# 第一步：顯示歡迎訊息
# ============================================================
print("=" * 50)
print("       個人資料卡製作器")
print("=" * 50)
print()

# ============================================================
# 第二步：收集基本資料（字串類型 str）
# ============================================================
# 字串是用來儲存文字的資料類型
# input() 函式返回的資料都是字串類型

print("【請輸入您的基本資料】")
print("-" * 50)

# 姓名（字串）
name = input("請輸入您的姓名：")

# 電子郵件（字串）
email = input("請輸入您的電子郵件：")

# 職業（字串）
job = input("請輸入您的職業：")

# ============================================================
# 第三步：收集數字資料（整數 int 和浮點數 float）
# ============================================================
# input() 返回的是字串，需要轉換成數字才能做運算
# int() 轉換成整數，float() 轉換成浮點數（小數）

print()
print("【請輸入您的數值資料】")
print("-" * 50)

# 年齡（整數）
# 年齡不會有小數，所以用 int() 轉換
age_input = input("請輸入您的年齡：")
age = int(age_input)  # 把字串 "25" 轉成整數 25

# 身高（浮點數）
# 身高可能有小數（如 170.5），所以用 float() 轉換
height_input = input("請輸入您的身高（公分）：")
height = float(height_input)  # 把字串 "170.5" 轉成浮點數 170.5

# 體重（浮點數）
weight_input = input("請輸入您的體重（公斤）：")
weight = float(weight_input)

# ============================================================
# 第四步：收集是非題資料（布林值 bool）
# ============================================================
# 布林值只有兩種：True（真）或 False（假）
# 常用於表示「是/否」的選項

print()
print("【請回答以下問題】")
print("-" * 50)

# 詢問是否為學生
student_answer = input("您是學生嗎？(y/n)：")
# 如果回答是 "y"，則 is_student 為 True
is_student = (student_answer == "y")

# 詢問是否有駕照
license_answer = input("您有駕照嗎？(y/n)：")
has_license = (license_answer == "y")

# ============================================================
# 第五步：計算衍生資料
# ============================================================
# 使用算術運算計算 BMI（身體質量指數）
# BMI = 體重(kg) / 身高(m)²

# 先把身高從公分轉成公尺
height_m = height / 100

# 計算 BMI
bmi = weight / (height_m * height_m)

# 計算出生年份（大約）
birth_year = 2024 - age

# ============================================================
# 第六步：輸出個人資料卡
# ============================================================
print()
print("=" * 50)
print("           您的個人資料卡")
print("=" * 50)

# 基本資料
print()
print("【基本資料】")
print(f"  姓名：{name}")
print(f"  電子郵件：{email}")
print(f"  職業：{job}")

# 數值資料
print()
print("【身體資料】")
print(f"  年齡：{age} 歲")
print(f"  出生年份：約 {birth_year} 年")
print(f"  身高：{height} 公分")
print(f"  體重：{weight} 公斤")
print(f"  BMI：{bmi:.1f}")  # .1f 表示保留一位小數

# 是非題資料
print()
print("【其他資訊】")
# 使用條件表達式來顯示「是」或「否」
student_text = "是" if is_student else "否"
license_text = "是" if has_license else "否"
print(f"  學生身份：{student_text}")
print(f"  持有駕照：{license_text}")

# ============================================================
# 第七步：顯示資料類型說明
# ============================================================
print()
print("=" * 50)
print("           變數類型說明")
print("=" * 50)
print()
print("【這個程式用到的變數類型】")
print(f"  name = '{name}'")
print(f" 類型：{type(name)}")  # type() 函式可以查看變數的類型
print()
print(f"age = {age}")
print(f" 類型：{type(age)}")
print()
print(f"  height = {height}")
print(f"  類型：{type(height)}")
print()
print(f"  is_student = {is_student}")
print(f"  類型：{type(is_student)}")

print()
print("=" * 50)
print("個人資料卡製作完成！")
print("=" * 50)


